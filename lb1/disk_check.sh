#!/bin/bash
# Скрипт перевіряє використання дискового простору у кореневому розділі "/"

USAGE=$(df / | tail -1 | awk '{print $5}' | sed 's/%//')

THRESHOLD=90

if [ "$USAGE" -ge "$THRESHOLD" ]; then
    echo "⚠️  УВАГА: Використано ${USAGE}% дискового простору у '/' (вільно менше 10%)"
else
    echo "✅ Все добре: використано ${USAGE}% дискового простору у '/'"
fi
